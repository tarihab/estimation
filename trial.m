
clear all
close all

[X,Y] = meshgrid(0:.05:1);
Z1 =  3*(1-X).^2.*exp(-(X.^2) - (Y+1).^2) ...
   - 10*(X/5 - X.^3 - Y.^5).*exp(-X.^2-Y.^2) ...
   - 1/3*exp(-(X+1).^2 - Y.^2);
Z2 =  3*(X).^2.*exp((-(X-0.7).^2 - (Y-0.7).^2)/0.05) ...
   + 1.*exp((-(X-0.4).^2-(Y-0.4).^2)/0.06) ...
   + 1/3*exp((-(X-0.2).^2 - (Y-0.2).^2)/0.08);
mesh(X,Y,Z2);
